template<typename T>
void read(vector<T>& v) {
	for(auto& x : v)
		cin >> x;
}

template<typename T>
void read(vector<vector<T>>& v) {
	for(auto& a : v)
		read(a);
}

template<typename T>
void read(T a[], int sz) {
	for(int i=0; i<sz; ++i)
		cin >> a[i];
}

class Edge {
public :
	int u, c;
	ll d;
	bool operator<(const Edge& e) const {
		return d>e.d;
	}
};

void solve() {
	int n, m;
	cin >> n >> m;
	vi cost(n);
	vector<vii> adj(n);
	vector<vector<bool>> vis(3001, vector<bool>(3001, false));
	read(cost);
	for(int i=0; i<m; ++i) {
		int u, v, c;
		cin >> u >> v >> c, --u, --v;
		adj[u].push_back({v, c});
		adj[v].push_back({u, c});
	}
	priority_queue<Edge> pq;
	pq.push({0, cost[0], 0});
	// 언제나 지금까지 지나오면서 가장 값 싼 도시에서
	// 기름을 많이 넣는 것이 유리함
	while(!pq.empty()) {
		auto [u, c, d]=pq.top();
		pq.pop();
		if(vis[u][c]) 
			continue;
		if(u==n-1) {
			print(d);
			return;
		}
		vis[u][c]=1;
		for(auto [v, nd] : adj[u]) 
			pq.push({v, min(c, cost[v]), c*nd+d});
	}
}

int main(int argc, char* argv[]) {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    solve();

    return 0;
}
