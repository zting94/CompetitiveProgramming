const int dx[4]={-1, 0, 1, 0}, dy[4]={0, -1, 0, 1};
const int mxN=1e5+1;
const ll M=1e9+9;
int N;
ll dp[mxN];

void pre() {
	dp[0]=dp[1]=1;
	dp[2]=dp[3]=2;
	dp[4]=dp[5]=3;
	for(int i=6; i<mxN; ++i)
		dp[i]=(dp[i-2]+dp[i-4]+dp[i-6])%M;
}

void solve() {
	cin >> N;
	print(dp[N]);
}

int main(int argc, char* argv[]) {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

	pre();

	int tt;
	cin >> tt;
	while(tt--)
		solve();

    return 0;
}
