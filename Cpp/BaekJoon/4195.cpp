const int mxN=2e5+1;
int N, p[mxN], cnt[mxN];

int find(int a) {
	if(p[a]==a)
		return a;
	return p[a]=find(p[a]);
}

void merge(int a, int b) {
	int pa=find(a);
	int pb=find(b);
	if(pa==pb)
		return;
	p[pb]=pa;
	cnt[pa]+=cnt[pb];
	cnt[pb]=0;
}

void solve() {
	memset(cnt, 0, sizeof(cnt));
	cin >> N;
	string a, b;
	unordered_map<string, int> mp;
	int last=0;
	for(int n=0; n<N; ++n) {
		cin >> a >> b;
		int i=-1, j=-1;
		if(mp.find(a)==mp.end()) {
			mp[a]=last;
			i=last++;
			p[i]=i;
			cnt[i]=1;
		} else {
			i=mp[a];
		}
		if(mp.find(b)==mp.end()) {
			mp[b]=last;
			j=last++;
			p[j]=j;
			cnt[j]=1;
		} else {
			j=mp[b];
		}
		assert(0<=i&&i<=2e5);
		assert(0<=j&&j<=2e5);
		merge(i, j);
		print(cnt[find(i)]);
	}
}

int main(int argc, char* argv[]) {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

	int tt;
	cin >> tt;
	while(tt--)
		solve();

    return 0;
}
